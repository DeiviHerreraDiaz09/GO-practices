package main

import "fmt"

func main() {

	// String
	var cadena string = "Hola, Mundo!"
	fmt.Println(cadena)

	// Integer
	var entero int = 42
	fmt.Println(entero)

	// Boolean
	var boolean bool = true
	fmt.Println(boolean)

	// Two variables in one line
	var numberOne, numberTwo int = 10, 20
	fmt.Println(numberOne, numberTwo)

	// Abbreviated variable declaration
	fruta := "Manzana" 
	fmt.Println(fruta)
	
}